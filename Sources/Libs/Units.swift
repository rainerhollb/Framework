import Foundation

struct Units {

   static let K : UInt64 = 1024
   static let M : UInt64 = K*K
   static let G : UInt64 = K*M
   static let T : UInt64 = M*M

   static let KB = "KB"
   static let MB = "MB"
   static let GB = "GB"
   static let TB = "TB"
   
}
